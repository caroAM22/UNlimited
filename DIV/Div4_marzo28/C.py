n = int(input())
d = {
    "12": "12",
    "13": "01",
    "14": "02",
    "15": "03",
    "16": "04",
    "17": "05",
    "18": "06",
    "19": "07",
    "20": "08",
    "21": "09",
    "22": "10",
    "23": "11",
    "24": "12",
}
for i in range(n):
    a, b = input().split(":")
    if int(a) >= 12:
        a = d[a]
        print(f"{a}:{b} PM")
    elif a == "00":
        print(f"12:{b} AM")
    else:
        print(f"{a}:{b} AM")
